namespace ModernFlyouts.Core.Media
{
    /// <summary>
    /// Specifies the direction in which a media playback's track change.
    /// </summary>
    public enum MediaPlaybackTrackChangeDirection
    {
        /// <summary>
        /// The change direction is unknown.
        /// </summary>
        Unknown = 0,

        /// <summary>
        /// The change direction is forward.
        /// </summary>
        Forward = 1,

        /// <summary>
        /// The change direction is backward.
        /// </summary>
        Backward = 2
    }
}

namespace ModernFlyouts.Core.Media
{
    /// <summary>
    /// Defines values for the types of media playback.
    /// </summary>
    public enum MediaPlaybackType
    {
        /// <summary>
        /// The media type is unknown.
        /// </summary>
        Unknown = 0,

        /// <summary>
        /// The media type is audio music.
        /// </summary>
        Music = 1,

        /// <summary>
        /// The media type is video.
        /// </summary>
        Video = 2,

        /// <summary>
        /// The media type is an image.
        /// </summary>
        Image = 3
    }
}
